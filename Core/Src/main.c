static inline uint32_t micros(void) { return __HAL_TIM_GET_COUNTER(&htim2); }

static void HCSR04_Trigger(void) {
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_SET);
  uint32_t t0 = micros();
  while ((uint32_t)(micros() - t0) < 12);  // >=10us
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_RESET);
}

// EXTI 콜백: PA8의 상승/하강 에지에서 불림
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
  if (GPIO_Pin == GPIO_PIN_8) {
    if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_8) == GPIO_PIN_SET) {
      // Rising edge
      echo_rise_us = micros();
      echo_done = 0;
      HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_SET);   // ★ 상승: LED ON
    } else {
      // Falling edge
      echo_fall_us = micros();
      echo_done = 1;  // 한 번 측정 완료
      HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET); // ★ 하강: LED OFF
    }
  }
}

